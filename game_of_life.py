def get_next_state(current_matrix: list[list[int]]):
    """Copy the current matrix world into a new one then apply the conways game of life rules on each cells of hte new one, finish by returning the new matrix world"""
    new_matrix: list[list[int]] = copy.deepcopy(current_matrix)

    for current_cell_pos_y in range(len(current_matrix)):
        for current_cell_pos_x in range(len(current_matrix[0])):
            total: int = get_number_of_living_neighboring_cells(
                current_matrix, current_cell_pos_x, current_cell_pos_y)

            if total == 3:
                new_matrix[current_cell_pos_x][current_cell_pos_y] = 1

            if total > 3 or total < 2:
                new_matrix[current_cell_pos_x][current_cell_pos_y] = 0
    return copy.deepcopy(new_matrix)


def get_number_of_living_neighboring_cells(matrix: list[list[int]], pos_x: int, pos_y: int) -> int:
    """Calculate the number of alive neighboring from a specific cell"""
    total: int = 0
    # Go thru the cells in the range one of the current cells
    for offset_x in range(-1, 2):
        for offset_y in range(-1, 2):
            # Dont add the current cell value, only its neighboring cells values
            if offset_y == 0 and offset_x == 0:
                continue

            # Current neighbor cell position
            neighbor_pos_x = pos_x + offset_x
            neighbor_pos_y = pos_y + offset_y

            # Check if the neighboring cell exist
            if (neighbor_pos_x < 0 or neighbor_pos_x >= len(matrix)) or (neighbor_pos_y < 0 or neighbor_pos_y >= len(matrix[0])):
                continue
            # Add the current neighbour cell value
            total += matrix[neighbor_pos_x][neighbor_pos_y]
    return total


def draw_matrix(matrix: list[list[int]], screen: pygame.Surface):
    """Function that draw a matrix of black or white rectangles depending on the value of the passed matrix"""
    screen_size_x, screen_size_y = pygame.display.get_window_size()
    cell_size_x = screen_size_x // len(matrix)
    cell_size_y = screen_size_y // len(matrix[0])
    for x in range(len(matrix)):
        for y in range(len(matrix[0])):
            pixel_value = 255 if matrix[x][y] == 1 else 0
            pygame.draw.rect(screen, (pixel_value, pixel_value, pixel_value), (cell_size_x*x,
                             cell_size_y*y, cell_size_x*(x+1), cell_size_y*(y+1)))


# Defining main function
def main():
    global current_matrix_world
    global simple_matrix_world
    pygame.init()
    clock = pygame.time.Clock()

    done: bool = False

    CELL_SIZE = 64
    screen: pygame.surface.Surface = pygame.display.set_mode(
        (len(simple_matrix_world)*CELL_SIZE, len(simple_matrix_world[0])*CELL_SIZE))
    # While the game is not over
    while not done:
        screen.fill((0, 0, 0))
        draw_matrix(simple_matrix_world, screen)  # type: ignore
        simple_matrix_world = get_next_state(simple_matrix_world)
        # Listen for all events
        for event in pygame.event.get():
            # Quit the infinite loop when the user presses the close button
            if event.type == pygame.QUIT:
                done = True
        clock.tick(2)
        pygame.display.flip()

    pygame.quit()

    pass


# Using the special variable
# __name__
if __name__ == "__main__":
    main()
