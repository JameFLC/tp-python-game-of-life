def get_next_state(matrix: list[list[int]]):
    new_matrix: list[list[int]] = copy.deepcopy(matrix)
    for y in range(len(matrix)):
        for x in range(len(matrix[0])):
            total: int = get_adjacent_total_value(matrix, x, y)
            if total == 3:
                new_matrix[x][y] = 1
            if total > 3 or total < 2:
                new_matrix[x][y] = 0
    return copy.deepcopy(new_matrix)


def get_adjacent_total_value(matrix: list[list[int]], pos_x: int, pos_y: int) -> int:
    total: int = 0
    for offset_x in range(-1, 2):
        for offset_y in range(-1, 2):
            if offset_y == 0 and offset_x == 0:
                continue
            x = pos_x + offset_x
            y = pos_y + offset_y
            if (x < 0 or x >= len(matrix)) or (y < 0 or y >= len(matrix[0])):
                continue
            total += matrix[x][y]
    return total


def draw_matrix(matrix: list[list[int]]):
    screen_size_x, screen_size_y = pygame.display.get_window_size()
    cell_size_x = screen_size_x // len(matrix)
    cell_size_y = screen_size_y // len(matrix[0])

    for x in range(len(matrix)):
        for y in range(len(matrix[0])):
            pixel_value = 255 if matrix[x][y] == 1 else 0
            pygame.draw.rect(screen, (pixel_value, pixel_value, pixel_value), (cell_size_x*x,
                             cell_size_y*y, cell_size_x*(x+1), cell_size_y*(y+1)))


pygame.init()

clock = pygame.time.Clock()

done: bool = False

CELL_SIZE = 16
screen: pygame.surface.Surface = pygame.display.set_mode(
    (len(initial_state)*CELL_SIZE, len(initial_state[0])*CELL_SIZE))
# While the game is not over
while not done:
    screen.fill((0, 0, 0))
    draw_matrix(initial_state)
    initial_state = get_next_state(initial_state)
    # Listen for all events
    for event in pygame.event.get():
        # Quit the infinite loop when the user presses the close button
        if event.type == pygame.QUIT:
            done = True
    pygame.display.flip()


pygame.quit()
